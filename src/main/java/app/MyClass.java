package app;

public class MyClass {

    String s;
    int n;

    public MyClass(String s, int n) {
        this.s = s;
        this.n = n;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }
}
